// Loops - a.	Display numbers from 1 to 50 

// Program to print numbers from 1 to 50.

#include<stdio.h>
int main()
{
    int i=1;
    printf(" \n\t Printing Numbers from 1 to 50 in a loop \n\n");
    for (i=1;i<=50;i++)
    {
        printf("%d",i);   
        printf("\n");
        }
        getch();
}
